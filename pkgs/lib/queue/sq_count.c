#include <queue.h>
#include <assert.h>

/****************************************************************************
 * Public Functions
 ****************************************************************************/

/****************************************************************************
 * Name: sq_count
 *
 * Description:
 *   Return the number of nodes in the queue.
 *
 ****************************************************************************/

size_t sq_count(sq_queue_t *queue) {
    sq_entry_t *node;
    size_t      count;

    assert(queue != NULL);

    for (node = queue->head, count = 0;
         node != NULL;
         node = node->flink, count++)
        ;

    return count;
}
